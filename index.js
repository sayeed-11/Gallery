let container = document.querySelector('.container');
let links = document.querySelectorAll('.nav-link');
let bar = document.querySelector('.bar');
let linkBox = document.querySelector('.links-box');
let icon = document.querySelector('.fa-solid');


const checkIcon = () => {
    if (icon.classList.contains('fa-bars')) {
        icon.classList.remove("fa-bars");
        icon.classList.add("fa-xmark");
    } else {
        icon.classList.add("fa-bars");
        icon.classList.remove("fa-xmark");
    }
}


bar.addEventListener('click', () => {
    linkBox.classList.toggle("active-bar");
    checkIcon();
})


links.forEach((link, index) => {
    link.addEventListener('click', (event) => {
        links.forEach((navLink, index) => {
            navLink.classList.remove('active');
        })
        link.classList.add('active');
        linkBox.classList.toggle("active-bar");
        checkIcon();

        const linkType = (link.children[0].innerHTML);
        setGalleryContent(linkType);
    })
})




const galleryConten = (list = images) => {
    container.innerHTML = "";
    list.forEach((item, index) => {
        let contentDiv = document.createElement('div');
        let imageTag = document.createElement('img');
        let icon = document.createElement('span');
        icon.innerHTML = `<i class="fa-regular fa-heart"></i>`
        imageTag.src = item.imgSrc;
        contentDiv.appendChild(imageTag);
        contentDiv.appendChild(icon);
        container.appendChild(contentDiv);
    })
}

document.addEventListener('DOMContentLoaded', () => galleryConten());

const setGalleryContent = (linkType) => {
    if (linkType == "ALL") {
        galleryConten(images);
    } else {
        let filterData = images.filter(item => item.type == linkType.toLowerCase());
        galleryConten(filterData);
    }

}
